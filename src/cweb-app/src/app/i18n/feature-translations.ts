function merge(base: FeatureTranslation, override: FeatureTranslation = {}): FeatureTranslation {
  const result: FeatureTranslation = {...base};
  for (const [key, value] of Object.entries(override)) {
    const current = result[key];
    result[key] = current && value && typeof current === 'object' && typeof value === 'object'
      ? merge(current as FeatureTranslation, value as FeatureTranslation)
      : value;
  }
  return result;
}

export function featureTranslation(locale: LocaleCode): Translation {
  return merge(
    merge(
      merge(FEATURE_TRANSLATIONS_ENGLISH, FEATURE_TRANSLATION_OVERRIDES[locale] ?? {}),
      FEATURE_TRANSLATION_COMPLETIONS[locale] ?? {}
    ),
    FEATURE_TRANSLATION_UI_COMPLETIONS[locale] ?? {}
  ) as Translation;
}
